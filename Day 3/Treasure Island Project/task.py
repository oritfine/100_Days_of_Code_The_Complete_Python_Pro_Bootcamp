print("Your mission is to find the treasure.")
direction = input("left ot right? ").lower()
if direction != "left":
    print("You fell into a hole.\nGame Over.")
else:
    swim_or_wait = input("swim or wait? ").lower()
    if swim_or_wait != "wait":
        print("You are attacked by trout.\nGame Over.")
    else:
        door = input("You reached 3 doors - red, yellow and blue. Which door you choose? ").lower()
        if door == "red":
            print("You are burned by fire.\nGame Over.")
        elif door == "blue":
            print("You are eaten by beasts.\nGame Over.")
        elif door == "yellow":
            print("You Win!")
        else:
            print("Game Over.")
